import sys
import subprocess
import os
from pathlib import Path

def main():
    current_dir = Path(__file__).parent.resolve()
    target_script = current_dir / "cli.py" 

    command = [sys.executable, str(target_script)] + sys.argv[1:]

    try:
        result = subprocess.run(command, check=False)
        sys.exit(result.returncode)
    except KeyboardInterrupt:
        print("\nCompiling interrupted by User.")
        sys.exit(130)
    except Exception as e:
        print(f"Couldn't start compiler: {e} (maybe it isn't installed fully or in diferrent directory. Check if it's not broken).")
        sys.exit(1)

if __name__ == "__main__":
    main()


